                <h1>MSn spectral tree databases: annotation and identification of plant metabolites</h1>
                <a href="./resources/Metabomeeting2012_September10_JJJvdHooft.pdf" target="_blank"><img style="border: thin solid #dcdcdc; float: right; margin: 2px 0 10px 10px;" src="./img/Metabomeeting2012_September10_JJJvdHooft.jpg"></a>
Annotation and identification of metabolites detected in untargeted liquid chromatography coupled to mass spectrometry (LC-MS) profiling
techniques is still a major challenge that needs to be overcome in order to interpret the results obtained in a biological context. Protocols for
generating MSn and LC-MSn data from metabolites detectable in plants, including Arabidopsis leaves and tomato fruit, have recently been
developed, resulting in robust spectral trees [1,2]. Meanwhile, a software tool called MEF has been developed to process the MSn raw data and
assign elemental formulas to the detected metabolite MSn signals [3]. Very recently, a web-based program called MetiTree, embedding the MEF
tool and algorithms to visualize and compare spectral trees [4] has become available [5].<br />
