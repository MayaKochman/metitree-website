                <h1>An automated Metabolite Identification Pipeline using Mass Spectral Trees</h1>
                <a href="./resources/Montreux2012_MI.pdf" target="_blank"><img style="border: thin solid #dcdcdc; float: right; margin: 2px 0 10px 10px;" src="./img/msn_montreux_2012.jpg"></a>
Identifying metabolites has been reported as one of the major bottlenecks in metabolomics. In part, this is due to the absence of
good computational tools to automate metabolite identification. To address this issue, we have developed mathematical tools to
process and compare multi-stage mass spectrometry data (MSn), in order to extract as much as possible information from the
fragmentation trees. In addition, candidate structures are generated computationally and filters are used to reject improbable
chemical structures. This poster presents the integrated use of these tools in a pipeline fashion to identify metabolites in human
urine.<br />
